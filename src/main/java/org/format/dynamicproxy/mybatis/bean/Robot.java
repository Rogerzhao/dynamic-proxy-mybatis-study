package org.format.dynamicproxy.mybatis.bean;


public class Robot {

    private Integer id;
    private String name;
    private int age;
    private Status status;

    public enum Status {
        DISABLE, ENABLE
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }
}
